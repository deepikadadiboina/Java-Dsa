package java2025;
import java.util.Scanner;

public class patternnew {
	public static void main (String args) {
		Scanner obj=new Scanner(System.in);
		System.out.println("enter n");
		int n=obj.nextInt();
		for(int i=0;i<n;i++) {
			for(int j=0;j<n;j++) {
				System.out.print("*");
			}
			//System.out.println();
		}
		System.out.print();
			
	}

}
